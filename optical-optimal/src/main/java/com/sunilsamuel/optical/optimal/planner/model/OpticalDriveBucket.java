package com.sunilsamuel.optical.optimal.planner.model;

import java.io.Serializable;
import java.math.BigInteger;

public class OpticalDriveBucket implements Serializable, Comparable<OpticalDriveBucket> {
	private static final long serialVersionUID = -5813641775223801439L;

	private Long size;
	private Integer id;

	/**
	 * @return the size
	 */
	public Long getSize() {
		return size;
	}

	/**
	 * @param size
	 *            the size to set
	 */
	public void setSize(Long size) {
		this.size = size;
	}

	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		result = prime * result + ((size == null) ? 0 : size.hashCode());
		return result;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		OpticalDriveBucket other = (OpticalDriveBucket) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		if (size == null) {
			if (other.size != null)
				return false;
		} else if (!size.equals(other.size))
			return false;
		return true;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		StringBuilder builder = new StringBuilder();
		builder.append("OpticalDriveBucket [size=").append(size).append(", id=").append(id).append("]");
		return builder.toString();
	}

	@Override
	public int compareTo(OpticalDriveBucket o) {
		if (o.getId() == this.getId()) {
			return 0;
		}
		return (o.getId() > this.getId() ? -1 : 1);
	}

}
